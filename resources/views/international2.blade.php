                <h2>Pernyataan Jaksa ICC Karim A.A. Khan KC: Aplikasi untuk surat perintah penangkapan terkait situasi di Negara Palestina<br><br></h2>
                <p>Pada 20 Mei 2024, Kantor saya mengajukan aplikasi untuk surat perintah penangkapan di hadapan Kamar Pra-Peradilan I terkait situasi di Negara Palestina.  
                    <br><br>
                    Hari ini, berdasarkan bukti yang diajukan oleh Kantor saya, hakim-hakim telah memastikan bahwa terdapat alasan yang masuk akal untuk percaya bahwa kejahatan berdasarkan Statuta Roma telah dilakukan.
                    <br><br>
                    Dalam kasus Mohammed Diab Ibrahim Al-Masri, yang lebih dikenal sebagai Deif, Panglima Tertinggi sayap militer Gerakan Perlawanan Islam “Hamas”, yang dikenal sebagai Brigade Al-Qassam, hakim Pengadilan Kriminal Internasional menemukan alasan yang masuk akal untuk percaya bahwa dia bertanggung jawab atas kejahatan terhadap kemanusiaan berupa pembunuhan, pemusnahan, penyiksaan, serta pemerkosaan dan kekerasan seksual lainnya; serta kejahatan perang berupa pembunuhan, perlakuan kejam, penyiksaan, penyanderaan, penghinaan atas martabat pribadi, dan pemerkosaan serta kekerasan seksual lainnya.
                    <br><br>
                    Dalam kasus Perdana Menteri Israel Benjamin Netanyahu dan mantan Menteri Pertahanan Israel Yoav Gallant, hakim Pengadilan Kriminal Internasional menemukan bahwa terdapat alasan yang masuk akal untuk percaya bahwa masing-masing telah melakukan kejahatan perang berupa penggunaan kelaparan sebagai metode perang dan kejahatan terhadap kemanusiaan berupa pembunuhan, penganiayaan, dan tindakan tidak manusiawi lainnya, sebagai pelaku langsung, bertindak bersama dengan orang lain. Kamar ini juga menemukan alasan yang masuk akal untuk percaya bahwa mereka masing-masing bertanggung jawab atas kejahatan perang berupa sengaja mengarahkan serangan terhadap warga sipil sebagai atasan.
                    <br><br>
                    Kantor saya juga telah mengajukan aplikasi untuk surat perintah penangkapan bagi Tuan Yahya Sinwar, saat itu Kepala Hamas di Jalur Gaza, dan Tuan Ismail Haniyeh, mantan Kepala Biro Politik Hamas, tetapi kemudian mencabut aplikasi tersebut setelah ada bukti yang mengonfirmasi kematian mereka.
                    <br><br>
                    Hari ini, perhatian kita bersama harus difokuskan pada para korban kejahatan internasional di Israel dan di Negara Palestina. Dalam pertemuan saya dengan korban dan keluarga sandera yang diambil dari kibbutzim, serta dengan korban dari Gaza yang telah kehilangan begitu banyak orang tercinta, saya telah menegaskan bahwa hukum ada untuk semua orang, bahwa perannya adalah untuk menegakkan hak semua individu.
                    <br><br>
                    Keputusan dari hakim independen Pengadilan Kriminal Internasional menegaskan bahwa hukum humaniter internasional harus ditegakkan dalam semua keadaan melalui proses peradilan yang adil dan tidak memihak.
                    <br><br>
                    Seperti yang saya tekankan pada bulan Mei, aplikasi ini dibuat setelah penyelidikan independen, dan berdasarkan bukti objektif dan dapat diverifikasi yang telah diperiksa melalui proses forensik.
                    <br><br>
                    Saya mengimbau semua Negara Pihak untuk memenuhi komitmen mereka terhadap Statuta Roma dengan menghormati dan mematuhi perintah peradilan ini. Kami mengandalkan kerja sama mereka dalam situasi ini, seperti halnya dengan situasi lain di bawah yurisdiksi Pengadilan. Kami juga menyambut kerja sama dengan negara-negara yang bukan Pihak untuk bekerja menuju akuntabilitas dan menegakkan hukum internasional.
                    <br><br>
                    Saya juga akan terus mencari kerja sama dari semua pemangku kepentingan, termasuk Negara Israel dan Negara Palestina, untuk memastikan Kantor saya sepenuhnya memenuhi tanggung jawabnya sesuai dengan pasal 54 Statuta Roma untuk menyelidiki keadaan yang memberatkan dan meringankan secara setara. Sesuai dengan Statuta Roma, pintu untuk komplementaritas terus terbuka. Seperti halnya semua situasi, kami akan terus secara aktif menilai penerapan prinsip fundamental ini, yang mengharuskan penyelidikan domestik yang asli dan penuntutan yang diperlukan terhadap individu yang sama untuk tindakan yang sama secara substansial.
                    <br><br>
                    Secara paralel, Kantor saya terus mengejar penyelidikan independen dan tidak memihaknya dalam situasi di Negara Palestina dengan fokus. Kami sedang melanjutkan penyelidikan di area di bawah yurisdiksi Pengadilan, yang mencakup Gaza dan Tepi Barat, termasuk Yerusalem Timur. Saya sangat prihatin dengan laporan kekerasan yang meningkat, semakin menyusutnya akses kemanusiaan, dan terus berkembangnya tuduhan kejahatan internasional di Gaza dan Tepi Barat.
                    <br><br>
                    Kami akan terus menjalankan mandat kami untuk memenuhi komitmen fundamental yang menjadi dasar Statuta Roma: bahwa kehidupan semua manusia memiliki nilai yang sama.
