                <h2>Netizen Indonesia Mengadu ke FIFA soal Wasit Bahrain vs Indonesia<br><br></h2>
                <p>Pertandingan matchday ketiga babak ketiga Grup C Kualifikasi Piala Dunia 2026 antara timnas Bahrain vs Indonesia memicu kemarahan suporter Indonesia yang mengadu ke FIFA terkait kepemimpinan wasit Ahmed Al Kaf.
                    <br><br>
                    Keputusan kontroversial dari wasit Al Kaf mewarnai laga Timnas Indonesia saat ditahan imbang Bahrain 2-2 di Stadion Nasional Bahrain, Riffa, Kamis (10/10).
                    <br><br>
                    Indonesia sempat unggul 2-1 pada menit ke-74 berkat gol indah Rafael Struick yang menggetarkan gawang Ebrahim Lutfalla. Namun, keunggulan tersebut sirna ketika Bahrain menyamakan kedudukan di menit 90+9, jauh melewati waktu tambahan yang seharusnya hanya 6 menit.
                    <br><br>
                    Keputusan wasit Ahmed Al Kaf asal Oman memperpanjang waktu pertandingan menjadi sorotan utama protes netizen Indonesia. Banyak yang menganggap keputusan ini tidak adil, mengingat tidak ada insiden signifikan yang terjadi selama waktu tambahan yang mengharuskan perpanjangan waktu hingga lebih dari 9 menit.
                    <br><br>
                    Selain masalah waktu tambahan, netizen juga mengkritik perilaku tim Bahrain yang dinilai sering mengulur waktu dengan reaksi berlebihan ketika terjadi bentrokan dengan pemain Timnas Indonesia.
                    <br><br>
                    Kekecewaan ini tercermin dalam berbagai komentar yang ditulis netizen Indonesia di unggahan akun Instagram resmi Piala Dunia FIFA.
                    <br><br>
                    “90+6=99 money money money money” tulis salah satu netizen.
                    <br><br>
                    “Prestasi Bahrain tidak pantas diapresiasi. Silakan wasit turun tangan dan bertanggung jawab atas semua kesalahan dan kecurangan Anda. Ini tidak hanya merugikan Bangsa dan Timnas kami, melainkan negaramu sendiri menjadi olok-olokan semua orang di muka bumi ini” timpal netizen lainnya
                    <br><br>
                    Tak sedikit pula yang menyoroti Konfederasi Sepak Bola Asia (AFC) yang dituding bekerja sama dengan wasit untuk menguntungkan Bahrain di laga kontra Indonesia.
                    <br><br>
                    “Ini tidak adil. #afcmafia money money money,” ungkap salah satu netizen.
                    <br><br>
                    “Apa menurut kalian mengenai Indonesia vs Bahrain, apakah Anda tidak melihat tambahan waktu 6 menit dan gol kedua Bahrain terjadi menit ke-99. Apakah FIFA selalu memprioritaskan uang daripada sportivitas? Kami suporter Indonesia sangat kecewa,” tulis netizen lainnya.
                    <br><br>
                    Selain itu tidak sedikit netizen Indonesia yang meminta FIFA untuk memeriksa wasit Ahmed Al Kaf asal Oman, yang dianggap terlalu menguntungkan tuan rumah Bahrain.
                    <br><br>
                    “Sepertinya FIFA harus bertindak dan melakukan investigasi pertandingan Indonesia vs Bahrain tadi malam. Pada pertandingan ini kinerja wasit jelas tidak netral dan lebih memihak ke tim Bahrain,” tulis netizen Indonesia.
                    <br><br>
                    “Ini sangat bobrok. Saya curiga wasit yang memimpin pertandingan Indonesia vs Bahrain itu curang, yang mengakibatkan banyak kerugian bagi pihak Indonesia. Apakah fifa akan bertindak?” tulis netizen lainnya.
                    <br><br>
