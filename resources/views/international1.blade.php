                <h2>Lebanon: Peringatan evakuasi Israel bagi warga sipil dianggap menyesatkan dan tidak memadai<br><br></h2>
                <p>Peringatan evakuasi yang dikeluarkan oleh militer Israel kepada warga pinggiran selatan Beirut dan Lebanon selatan dianggap tidak memadai, dan dalam beberapa kasus juga menyesatkan, ungkap Amnesty International hari ini. Amnesty menyoroti bahwa peringatan ini tidak membebaskan Israel dari kewajiban mereka berdasarkan hukum humaniter internasional untuk tidak pernah menargetkan warga sipil dan mengambil semua langkah yang mungkin untuk meminimalkan kerugian terhadap mereka.
                    <br><br>
                    Berdasarkan hukum humaniter internasional, pihak-pihak yang terlibat konflik memiliki kewajiban jelas untuk mengambil semua tindakan pencegahan yang memungkinkan untuk menghindari, atau setidaknya meminimalkan, kerugian terhadap warga sipil saat melakukan serangan; ini termasuk memberikan peringatan awal yang efektif terhadap serangan di area yang terkena dampak kecuali dalam keadaan yang tidak memungkinkan.
                    <br><br>
                    “Peringatan yang dikeluarkan oleh militer Israel kepada warga Dahieh, kawasan padat penduduk di pinggiran selatan Beirut, tidak memadai. Analisis kami menunjukkan bahwa tidak hanya peringatan tersebut disertai peta yang menyesatkan, tetapi juga dikeluarkan dalam waktu singkat – dalam satu kasus kurang dari 30 menit sebelum serangan dimulai – di tengah malam, melalui media sosial, saat banyak orang sedang tidur, offline, atau tidak mengikuti laporan media,” ujar Agnès Callamard, Sekretaris Jenderal Amnesty International.
                    <br><br>
                    “Lebih lanjut, memerintahkan warga dari seluruh kota dan desa di Lebanon selatan untuk mengungsi adalah peringatan yang terlalu umum, tidak memadai, dan menimbulkan pertanyaan apakah hal ini dimaksudkan untuk menciptakan kondisi pengungsian massal. Terlepas dari keefektifan peringatan tersebut, hal ini tidak berarti bahwa Israel dapat memperlakukan warga sipil yang tersisa sebagai target. Orang-orang yang memilih untuk tetap tinggal di rumah mereka atau tidak dapat pergi karena anggota keluarga mereka memiliki mobilitas terbatas, karena disabilitas, usia, atau alasan lain, tetap dilindungi oleh hukum humaniter internasional. Israel harus mematuhi kewajibannya setiap saat berdasarkan hukum internasional, termasuk dengan mengambil semua tindakan pencegahan yang memungkinkan untuk meminimalkan kerugian terhadap warga sipil, di mana pun mereka berada.”
                    <br><br>
                    Menurut Kantor Komisaris Tinggi PBB untuk Hak Asasi Manusia (OCHA), seperempat wilayah Lebanon telah terkena dampak dari peringatan evakuasi ini.
                    <br><br>
                    Untuk menjadi efektif, sebuah peringatan harus tepat waktu dan memberikan informasi tentang jalur dan tujuan yang aman. Amnesty International meninjau dua peringatan yang dikeluarkan kepada warga kawasan urban padat penduduk Dahieh pada malam 27/28 September, setelah serangan mendadak yang menewaskan pemimpin Hezbollah, Hassan Nasrallah. Serangan udara tersebut menghancurkan seluruh gedung perumahan di daerah yang padat penduduk. Setiap peringatan mengidentifikasi tiga target militer dan meminta warga untuk mengungsi dalam radius 500 meter di sekitar lokasi tersebut. Peringatan tersebut dikeluarkan melalui juru bicara militer Israel dalam bahasa Arab di X (sebelumnya Twitter), pada malam hari, tanpa jadwal waktu yang jelas atau rincian tentang jalur aman.
                    <br><br>
                    Dalam dua peringatan yang dikeluarkan untuk warga Dahieh, peta yang diterbitkan oleh militer Israel bersama peringatan evakuasi tersebut, yang mencakup enam area berbeda, dianggap menyesatkan. Dalam setiap kasus ini, area yang disorot pada peta untuk menunjukkan zona bahaya bagi warga sipil mencakup area yang jauh lebih kecil daripada radius 500 meter yang disarankan oleh militer Israel untuk dihindari warga sipil.
                    <br><br>
                    Peringatan ini tidak memberikan petunjuk yang jelas bagi warga sipil tentang cara menjauh dari tujuan militer yang akan menjadi target serangan. Meskipun peringatan bisa bersifat umum, definisi dari "umum" tidak mencakup peringatan yang terlalu luas dan meminta warga sipil untuk mengungsi dari seluruh wilayah.
