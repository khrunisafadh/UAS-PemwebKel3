                <h2>Kulit Kepala Berminyak Rambut Kering? Ini Tips Perawatan yang Tepat<br><br></h2>
                <p>Meski aneh, tapi kulit kepala berminyak dan batang rambut sampai ujung terasa kering memang banyak terjadi. Kondisi ini memerlukan perawatan yang tepat.
                    Umumnya, produk perawatan rambut menawarkan formula sesuai tipe rambut seperti rambut kering, berminyak, atau berketombe. Namun, ketika kondisinya kulit kepala berminyak tapi rambut kering, rutinitas merawat rambut bisa jadi serba salah.
                    <br><br>
                    "Ada banyak kemungkinan kondisi seperti kulit kepala berminyak dengan rambut kering, kulit kepala kering gatal dengan rambut berminyak, rambut yang sebagian kusut dan sebagian berminyak, rambut kering rusak dengan kulit kepala berminyak, atau akar berminyak dengan ujung kering," ujar penata rambut dan penata rias Florian Hurel, mengutip dari Vogue India.
                    <br><br>
                    Perawatan tepat buat kulit kepala berminyak dan rambut kering
                    Hurel menyebut kondisi ini sebagai tipe rambut kombinasi. Berikut beberapa tips perawatan rambut kombinasi tanpa membuatnya makin berminyak atau makin kering.
                    <br><br>
                    <b>1. Pilih sampo yang tepat</b><br>
                    Sebaiknya pilih sampo yang memang diperuntukkan bagi kulit kepala berminyak. Produk ini bisa mengurangi minyak berlebih tanpa membuat kulit kepala kering.Mengutip berbagai sumber, pilih eksfoliator dengan bahan antara lain tea tree oil, witch hazel, atau salicylic acid. Bahan-bahan ini dapat menyeimbangkan minyak di kulit kepala tanpa membuatnya kering.
                    <br><br>
                    <b>2. Hindari panas berlebih</b><br>
                    Cara mengeringkan rambut juga perlu diperhatikan. Hurel menyarankan untuk mengeringkan rambut dengan udara atau penggunaan alat pemanas dengan pengaturan panas rendah.
                    <br><br>
                    Cara ini digunakan agar kulit kepala tidak semakin berminyak akibat rangsangan panas. Selain itu, ujung rambut tidak terpapar panas tinggi dan jadi semakin kering.
                    <br><br>
                    <b>3. Ikat rambut</b><br>
                    Terlalu sering keramas bisa memicu produksi minyak berlebih. Sebenarnya saat belum keramas, pilih gaya rambut yang memastikan rambut terangkat dan terhindar dari kontak dengan minyak di kulit kepala.
                    <br><br>
                    Pertimbangkan untuk mengikat, mengepang, atau gulung (sanggul) rambut agar membantu distribusi minyak dan mencegah ujung rambut kering.
                    <br><br>
                    <b>4. Trimming reguler</b><br>
                    Trimming atau memotong ujung rambut perlu rutin dilakukan tiap 6-8 minggu sekali. Trimming akan membuang ujung rambut yang bercabang dan rusak sehingga rambut tetap sehat dan segar.
                </p>
