                    <br><br>
                    Bob Bryar menjadi drummer My Chemical Romance dari tahun 2004 hingga 2010. dia meninggalkan My Chemical Romance pada tahun 2010 sebelum grup tersebut bubar pada tahun 2013.
                    <br><br>
