                <h2>Iran Tambah 6.000 Sentrifus Baru di Pabrik Pengayaan Uranium Nuklir<br><br></h2>
                <p>Badan Nuklir PBB mengonfirmasi bahwa Iran berencana memasang sekitar 6.000 sentrifus baru untuk memperkaya uranium.
                    Dilansir AFP, Iran disebut sudah memberi tahu pengawas nuklir PBB bahwa mereka berencana memasang lebih dari 6.000 sentrifus pengayaan uranium tambahan, dan mengaktifkan lebih banyak sentrifus yang sudah ada.
                    <br><br>
                    Laporan Badan Energi Atom Internasional (IAEA) yang dilihat Reuters merinci bahwa rencana Iran ini sebagai tanggapan atas resolusi yang disahkan oleh Dewan Gubernur IAEA pekan lalu atas permintaan Inggris, Prancis, Jerman, dan Amerika Serikat.
                    <br><br>
                    Kapasitas pengayaan uranium yang lebih besar berarti Iran bisa memperkaya uranium lebih cepat, yang berpotensi meningkatkan risiko proliferasi nuklir Iran.
                    <br><br>
                    Iran menyangkal bahwa mereka tengah berupaya membuat senjata nuklir. Namun negara-negara Barat menilai tidak ada penjelasan yang masuk akal terkait pengayaan uranium hingga kemurnian 60 persen,
                    <br><br>
                    Untuk dikategorikan sebagai senjata nuklir, pengayaan uranium harus mendekati angka sekitar 90 persen.
                    <br><br>
                    Sejauh ini Iran telah memiliki lebih dari 10 ribu sentrifus yang beroperasi di dua pabrik bawah tanah, yakni di Natanz dan Fordow.
                    <br><br>
                    Pabrik Fordow diawasi secara ketat karena digali di gunung, dan Iran saat ini memperkaya hingga 60% uranium di sana. Satu-satunya pabrik lain yang melakukan itu adalah Pabrik Pengayaan Bahan Bakar Percontohan di atas tanah di Natanz.

