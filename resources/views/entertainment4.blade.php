                <h2>Karakter Gong Yoo di Drakor The Trunk Bikin Iba, Ini Pesan dari Sutradara<br><br></h2>
                <p>Drama Korea The Trunk yang dibintangi Gong Yoo dan Seo Hyun Jin tayang eksklusif hari ini, Jumat (29/11/2024) di Netflix. Berbeda dengan karakter yang dia perankan sebelumnya, Gong Yoo bergulat dengan rasa kesedihan dan kesepian. Sampai-sampai ketergantungan obat tidur.
                    Alur cerita The Trunk diadaptasi dari sebuah novel karya penulis Kim Ryeo Ryeong dengan arahan sutradara kenamaan Korea, Kim Kyu Tae. Ialah sutradara yang berada di balik kesuksesan drama Korea Our Blues dan It's Okay That's Love. Lewat karyanya, Kim Kyu Tae mengangkat kisah-kisah yang menghangatkan hati dengan sinematografi memukau.
                    <br><br>
                    Dalam drama Korea The Trunk, penggemar akan melihat transformasi Gong Yoo sebagai produser musik bernama Han Jeong Won, yang berat hati menyetujui pernikahan sementara atas perintah mantan istrinya sebagai 'hukuman'. Karakter Gong Yoo kerap mengalami mimpi buruk dan insomnia yang disebabkan oleh trauma masa lalunya.
                    <br><br>
                    Meskipun awalnya bersikap tidak ramah terhadap istri keduanya Noh In Ji (pemain Seo Hyun Jin), Han Jeong Won perlahan mulai menyadari kehangatan yang dibawa wanita asing itu ke dalam kehidupannya.
                    <br><br>
                    Sutradara Kim Kyu Tae tak menyangka akan melihat Gong Yoo memerankan karakter yang kekanak-kanakan. Aktor 45 tahun itu keluar dari zona nyamannya. Selama ini, dia memancarkan aura pria matang dan dewasa.
                    <br><br>
                    "Aku rasa ini sedikit di luar perkiraan bahwa Gong Yoo akan memerankan Jeong Won karena Jeong Won adalah karakter yang sangat menyedihkan dan kekanak-kanakan dan dia juga pemarah. Dan bagaimana Gong Yoo mengekspresikan karakter ini adalah salah satu yang hal membuatku penasaran. Seperti yang kita semua ketahui, sangat sulit mencocokkan karakter itu dengan Gong Yoo karena aura yang dimilikinya," ungkap Kim Kyu Tae dalam wawancara eksklusif media bersama Netflix, Rabu (20/11/2024).
                    <br><br>
                    Kim Kyu Tae menambahkan bahwa Gong Yoo berhasil keluar dari zona nyamannya, bahkan membuat karakter Han Jeong Won terlihat lebih keren dari yang seharusnya.
                    <br><br>
                    "Namun ternyata, seperti yang dia katakan, karakter itu seperti magnet untuknya. Dia sangat cocok dengan karakter itu dan ketika adegan itu ditampilkan di layar oleh seorang aktor dan aktor itu mengekspresikannya, teksturnya tiba-tiba berbeda. Sangat menyenangkan melihat bagaimana hal itu diwujudkan oleh Gong Yoo. Bahkan karakter itu menjadi sangat keren dan itu seharusnya tidak terjadi pada karakter tersebut."
                    <br><br>
                    Menurutnya, penonton drama Korea The Trunk khususnya kaum wanita akan iba dan bersimpati dengan karakter Gong Yoo. "Aku rasa karakter Han Jeong Won akan memancing jiwa keibuan penonton wanita dan rasa kasih sayang dalam diri mereka."
                    <br><br>
                    Di akhir wawancara, sutradara drama Korea The Trunk mengungkapkan harapannya agar penonton dapat menemukan cara berdamai dengan kesedihan dan kesepian.
                    <br><br>
                    "Aku ingin drama ini melahirkan pertanyaan kepada penonton 'Bagaimana kita bisa menerima kesepian, bukan menolaknya, tetapi memiliki pikiran yang sehat untuk menemukan jalan keluar, sehingga kita bisa berkomunikasi dengan lebih baik dan mencintai dengan lebih baik'. Aku harap pesan itu tersampaikan lewat drama ini," pungkas Kim Kyu Tae. </p>
