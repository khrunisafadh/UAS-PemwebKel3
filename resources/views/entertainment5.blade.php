                <h2>7 Drakor Terbaru Tayang Desember 2024, Ada Who Is She dan Squid Game 2<br><br></h2>
                <p>Siapa nih yang gak sabar nungguin deretan drakor baru buat nemenin akhir tahun? Desember 2024 bakal jadi bulan yang seru banget buat yang suka maraton drama Korea.
                    Mulai dari cerita balas dendam, komedi romantis, sampai thriller penuh intrik, semuanya bakal hadir dan bikin kamu gak bisa jauh dari layar.
                    <br><br>
                    Yang paling dinanti, tentu aja Squid Game 2!
                    <br><br>
                    Setelah tiga tahun bersabar, akhirnya kita bisa lihat lagi aksi Seong Gi Hun (Lee Jung Jae) di permainan maut itu. Tapi gak cuma itu, ada juga beberapa judul lain yang gak kalah menarik.
                    <br><br>
                    <b>Ini 7 Drakor Baru Tayang Desember 2024</b>
                    <br><br>
                    <b>1. Cinderella Game</b><br>
                    Mulai tayang: 2 Desember 2024<br>
                    Platform: KBS2<br>
                    Cast: Han Groo, Choi Sang<br><br>
                    
                    Drama ini kayaknya bakal penuh plot twist! Koo Ha Na (Han Groo) yang jadi tulang punggung keluarga, tiba-tiba tahu kalau dia bukan anak kandung.
                    <br><br>
                    <b>2. Light Shop</b><br>
                    Mulai tayang: 4 Desember 2024<br>
                    Platform: Disney+ Hotstar<br>
                    Cast: Ju Ji Hoon, Park Bo Young<br><br>
                    
                    Ini bukan toko lampu biasa, gengs. Setiap arwah yang mampir ke toko ini punya kisah yang bikin merinding. Drama ini adalah adaptasi dari webtoon terkenal, jadi ceritanya udah pasti solid. Plus, siapa sih yang gak kangen sama akting Ju Ji Hoon dan Park Bo Young?
                    <br><br>
                    <b>3. Sorry Not Sorry</b><br>
                    Mulai tayang: 5 Desember 2024<br>
                    Platform: KBS Joy & Netflix<br>
                    Cast: Jeon So Min, Gong Min Jung<br><br>
                    
                    Sorry Not Sorry kayaknya bakal jadi pilihan tepat buat yang suka drama slice of life. Tiga wanita dengan masalah masing-masing, mulai dari cinta gagal, konflik sahabat, sampai perjuangan jadi ibu pekerja. Drama ini relatable banget, deh!
                    <br><br>
                    <b>4. Who Is She</b><br>
                    Mulai tayang: 18 Desember 2024<br>
                    Platform: KBS2<br>
                    Cast: Kim Hae Sook, Jung Ji So, Jinyoung<br><br>
                    
                    Siapa sih yang gak tahu Miss Granny? Nah, versi drama ini bakal menghidupkan kembali kisah nenek yang mendadak muda lagi.
                    <br><br>
                    Oh Mal Soon (Kim Hae Sook) jadi Oh Doo Ri (Jung Ji Soo) dan mulai mengejar mimpinya jadi penyanyi. Yang bikin makin seru, ada bumbu romansa dengan produser ganteng Daniel Han (Jinyoung).
                    <br><br>
                    <b>5. Check In Hanyang</b><br>
                    Mulai tayang: 21 Desember 2024<br>
                    Platform: Channel A & Netflix<br>
                    Cast: Bae In Hyuk, Kim Ji Eun<br><br>
                    
                    Drama ini kayak fusion antara historical drama dan komedi modern. Empat anak muda yang bekerja di wisma tamu kerajaan dengan berbagai masalah dan penyamaran.
                    <br><br>
                    <b>6. Namib</b><br>
                    Mulai tayang: 23 Desember 2024<br>
                    Platform: ENA & Genie TV<br>
                    Cast: Ko Hyun Jung, Ryeo Un<br><br>
                    
                    Dari seorang sutradara terkenal yang dipecat, sampai proyek pencarian idol berbakat, Namib adalah drama yang menggabungkan perjuangan dan mimpi. Chemistry antara Ko Hyun Jung dan Ryeo Un pasti bikin hati kamu anget.
                    <br><br>
                    <b>7. Squid Game 2</b><br>
                    Mulai tayang: 26 Desember 2024<br>
                    Platform: Netflix<br>
                    Cast: Lee Jung Jae, Lee Byung Hun<br><br>
                    
                    Akhirnya, setelah penantian panjang, Squid Game 2 hadir. Kali ini, Seong Gi Hun kembali ke arena permainan dengan rencana yang bikin penasaran.
