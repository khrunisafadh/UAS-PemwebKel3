                <h2>Bos Facebook Mark Zuckerberg Bertemu Donald Trump, Apa yang Dibahas?<br><br></h2>
                <p>Bos raksasa teknologi Meta, Mark Zuckerberg, bertemu dengan presiden AS terpilih Donald Trump di resornya di Mar-a-Lago. Apa yang dibahas keduanya?
                    Andy Stone, Juru Bicara Meta, mengatakan bahwa pertemuan ini adalah "hal yang penting bagi masa depan inovasi Amerika". Stone juga mengatakan Mark berterima kasih atas undangan makan malam dari Trump.
                    <br><br>
                    "Mark berterima kasih atas undangan Presiden Trump untuk makan malam dan kesempatan bertemu dengan anggota timnya tentang pemerintahan yang akan datang," kata Stone dalam keterangannya, melansir The Verge, Rabu (27/11).
                    <br><br>
                    Stephen Miller, penasihat senior Trump, juga mengonfirmasi pertemuan tersebut. Menurut dia dalam pertemuan tersebut Zuckerberg mengutarakan keinginannya untuk mendukung pemerintahan Trump.
                    <br><br>
                    "Mark Zuckerberg menyatakan dengan sangat jelas keinginannya untuk menjadi pendukung, dan ikut serta dalam perubahan di seluruh Amerika, di seluruh dunia, dengan gerakan reformasi yang dipimpin oleh Donald Trump," kata Miller.
                    <br><br>
                    Sementara itu, Zuckerberg dengan sengaja menjauhkan diri dari politik tahun ini, meskipun ia mengatakan bahwa Trump yang selamat dari percobaan pembunuhan adalah "salah satu hal paling hebat yang pernah saya lihat dalam hidup saya."
                    <br><br>
                    Ia juga merupakan salah satu dari sekian banyak CEO teknologi yang mengucapkan selamat kepada Trump atas kemenangannya dalam pemilu 2024, dengan mengatakan dalam sebuah unggahan di Threads bahwa ia "menantikan untuk bekerja sama dengan Anda dan pemerintahan Anda.
