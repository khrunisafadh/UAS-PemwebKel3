                <h2>Huawei Bawa Lagi Hp Flagship ke Indonesia, Intip Spek dan Harganya<br><br></h2>
                <p>Huawei membawa kembali ponsel flagshipnya ke Indonesia lewat Huawei Pura 70 Ultra. Salah satu fitur andalannya adalah Ultraspeed Snapshot yang bisa memotret objek yang bergerak dengan cepat seperti kereta Whoosh.
                    "Extreme condition-nya untuk ultra speed snapshot itu seperti apa? Main camera itu dia bisa mengambil maksimum shooting speed sampai dengan 200 kilometer per jam," ujar Training Director Huawei Device Indonesia Edy Supartono di Kantor Huawei Indonesia, Jakarta, Kamis (28/11).
                    <br><br>
                    Huawei Pura 70 Ultra hadir dengan layar 6.8 inch OLED LTPO resolusi 2844 × 1260 pixels, refresh rate hingga 120 Hz, 1.07 miliar warna, P3 wide color gamut, 1440Hz high-frequency PWM dimming, dengan kecerahan hingga 2.500 nits.
                    <br><br>
                    Layar ini diberi perlindungan kaca tempered Xuanwu Kunlun. Huawei mengklaim Pura 70 Ultra memiliki ketahanan jatuh yang meningkat hingga 100 persen dan ketahanan gores yang meningkat hingga 300 persen.
                    <br><br>
                    Dibekali dengan chipset Kirin 9010, ponsel ini hadir dengan konfigurasi memori 16/512GB dan 16GB/1TB. Sistem operasinya sendiri menggunakan EMUI 14 berbasis android.
                    <br><br>
                    Pada bagian belakang, Huawei Pura 70 Ultra membawa kamera 50 MP 1 inci, wide, dengan aperture F1.6-F4.0; kamera 40 MP ultrawide f/2.2; serta lensa telefoto super-makro 50 MP f/2/1.
                    <br><br>
                    Sensor utama didukung stabilisasi Shift OIS, sementara kamera telefoto memiliki stabilisasi gambar optik. Telefoto menyediakan zoom optik hingga 3,5x dan dapat didorong ke zoom digital 100x.
                    <br><br>
                    Sementara itu, kamera depannya dibekali dengan lensa 13MP.
                    <br><br>
                    Untuk menunjang performanya, ponsel ini dibekali baterai 5.200 mAh dengan dukungan pengisian daya cepat 100W. Ponsel ini juga dibekali fitur wireless charging 80W, serta 18W wired/20W wireless reverse charging
                    <br><br>
                    Ponsel dengan dimensi 162.6 × 75.1 × 8.4 mm dan berat 226 gram hadir dengan desain tepi layar lengkung. Pada bagian belakang, ponsel ini dibalut oleh kulit vegan.
                    <br><br>
                    <b>Harga</b><br>
                    Huawei sudah menyertakan daftar harga untuk ponsel flagshipnya. Setiap pembelian Huawei Pura 70 Ultra akan mendapatkan bonus Huawei Watch GT 4 serta dua tahun layanan ekstra garansi. Berikut harga Huawei Pura 70 Ultra di Indonesia:
                    <br><br>
                    Huawei Pura 70 Ultra Black 16 GB/512 GB Rp17,999 juta<br>
                    Huawei Pura 70 Ultra Green 16 GB/1TB Rp20,999 juta
