                <h2>Kajian Bersama tentang Konektivitas Energi Regional oleh AS dan Singapura Dapat Menjadi Panduan bagi Kerangka Jaringan Listrik ASEAN: PM Wong<br><br></h2>
                <p><b>PERNYATAAN TENTANG “KAJIAN KERANGKA HUKUM DAN PENDANAAN UNTUK KONEKTIVITAS ENERGI REGIONAL DI ASIA TENGGARA”, MELALUI INISIATIF NET ZERO WORLD</b>
                    <br><br>
                    1. Kementerian Perdagangan dan Industri (MTI) dan Departemen Energi Amerika Serikat
                    (DOE) telah memutuskan syarat dan pendekatan untuk fase selanjutnya dari Kajian Kelayakan
                    Bersama Amerika Serikat – Singapura tentang Konektivitas Energi Regional melalui Inisiatif Net Zero World.
                    <br><br>
                    2. Pada 3 April 2023, Singapura dan Amerika Serikat memulai fase pertama dari
                    kajian kelayakan bersama tentang konektivitas energi regional, dengan memanfaatkan jaringan
                    ahli laboratorium nasional dari Inisiatif Net Zero World. Kedua negara akan segera memulai
                    “Kajian tentang Kerangka Hukum dan Pendanaan untuk Konektivitas Energi Regional di Asia Tenggara”,
                    yang akan melibatkan lebih banyak negara mitra.
                    <br><br>
                    3. Singapura dan Amerika Serikat dengan senang hati mengumumkan pembentukan
                    Kelompok Kerja untuk mendukung kajian ini dan memajukan tujuan bersama kami dalam Jaringan
                    Listrik ASEAN (ASEAN Power Grid). Dikoordinasikan oleh AS dan Singapura di bawah kemitraan Net Zero World, 
                    Kelompok Kerja ini mencakup negara-negara regional dan mitra multilateral, yang menyatukan pemangku
                    kepentingan publik dan swasta seperti pakar industri, pembuat kebijakan, dan peneliti dari berbagai sektor energi.
                    Kelompok Kerja ini memungkinkan peserta dan anggota untuk berbagi praktik terbaik, memperoleh wawasan penting,
                    dan berkontribusi membangun fondasi untuk Jaringan Listrik ASEAN. Kami mendorong keterlibatan aktif dan
                    menyambut kontribusi dari semua pemangku kepentingan saat kami memulai perjalanan penting ini.
                    <br><br>
                    4. Kedua negara menantikan dimulainya kajian ini pada Singapore International Energy Week (SIEW) pada Oktober 2024.
                    <br><br>
                    5. Singapura dan Amerika Serikat juga menantikan pendalaman kerja sama mereka melalui peluncuran Pusat
                    Kerjasama Regional pertama International Energy Agency (IEA) pada SIEW, yang akan berlokasi di Singapura.
                    Diumumkan pada Ministerial ke-50 IEA pada Februari 2024, Pusat baru ini dibangun di atas partisipasi lama
                    Singapura di IEA sebagai negara Asosiasi dan akan menjadi organisasi internasional pertama yang berfokus pada energi di Singapura.
                    Amerika Serikat memberikan dukungan khusus dan berkelanjutan untuk penciptaan dan peluncuran Pusat ini,
                    yang bertujuan bekerja sama dengan semua negara di Asia Tenggara untuk mempercepat transisi energi bersih mereka
                    sambil meningkatkan keamanan energi regional dan global.
